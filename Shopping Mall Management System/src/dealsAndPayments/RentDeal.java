    private void tenantButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tenantButtonActionPerformed

        String name = "";
        String referenceName = "";
        String referenceId = "";
        tenantId = tenantTextField.getText();
        try {
            statement = con.createStatement();
            resultSet = statement.executeQuery("SELECT name,reference_id FROM `tenant` WHERE tenant_id=" + tenantId);
            while (resultSet.next()) {
                name = resultSet.getString(1);
                referenceId = resultSet.getString(2);

            }
            resultSet = statement.executeQuery("SELECT name FROM `reference` WHERE reference_id=" + referenceId);
            while (resultSet.next()) {
                referenceName = resultSet.getString(1);
            }
            if (!name.isEmpty() && name != null) {
                nameLabel.setText("Tenant Name: " + name);
                referenceNameLabel.setText("Referenced Name: " + referenceName);
                tenantButton.setVisible(false);
                tenantTextField.setEditable(false);
                counter++;
            } else {
                JOptionPane.showMessageDialog(this, "This Tenant is Not Available!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Enter Valid Tenant ID");
        }

        if (counter >= 2) {
            dealButton.setVisible(true);
        }
    }//GEN-LAST:event_tenantButtonActionPerformed

