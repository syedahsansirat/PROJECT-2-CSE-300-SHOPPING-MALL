    private void shopButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shopButtonActionPerformed
        String idFromDB = "";
        String roomNo = "";
        String securityFee = "";
        String rent = "";
        shopId = shopTextField.getText();
        try {
            statement = con.createStatement();
            resultSet = statement.executeQuery("SELECT shop_id FROM `rent` WHERE shop_id=" + shopId);
            while (resultSet.next()) {
                idFromDB = resultSet.getString(1);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        if (!idFromDB.isEmpty() && idFromDB != null && idFromDB.matches(shopId)) {
            JOptionPane.showMessageDialog(this, "Shop is Not Available");
        } else {
            try {
                statement = con.createStatement();
                resultSet = statement.executeQuery("SELECT room_no,security_fee,rent FROM `shop` WHERE shop_id=" + shopId);
                while (resultSet.next()) {
                    roomNo = resultSet.getString(1);
                    securityFee = resultSet.getString(2);
                    rent = resultSet.getString(3);
                }
                if (!rent.isEmpty() && rent != null) {
                    roomNoLabel.setText("Room Number: " + roomNo);
                    securityLabel.setText("Security Fee:" + securityFee);
                    rentLabel.setText("Monthly Rent: " + rent);
                    shopButton.setVisible(false);
                    availablityLabel.setText("Shop Available...");
                    shopTextField.setEditable(false);
                    counter++;
                } else {
                    JOptionPane.showMessageDialog(this, "This Shop is Not Available!");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Enter Shop ID");
            }

        }
        if (counter >= 2) {
            dealButton.setVisible(true);
        }

    }//GEN-LAST:event_shopButtonActionPerformed

